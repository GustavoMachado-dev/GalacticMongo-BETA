package com.example.tomticketapi1.structure.domain.entities.enums;

/**
 * A enumeração Prioridade representa os diferentes níveis de prioridade
 * que podem ser atribuídos a um item ou chamado no sistema.
 */
@SuppressWarnings("unused")
public enum Prioridade {

    /**
     * Representa uma prioridade baixa.
     */
    BAIXO,

    /**
     * Representa uma prioridade normal.
     */
    NORMAL,

    /**
     * Representa uma prioridade alta.
     */
    ALTO,

    /**
     * Representa uma prioridade urgente.
     */
    URGENTE
}
